class TranslationService extends ChangeNotifier {
  static final TranslationService _instance = TranslationService._internal();
  factory TranslationService() => _instance;
  TranslationService._internal();

  String _currentLanguage = 'Français';
  String get currentLanguage => _currentLanguage;

  String _currentCurrency = 'FCFA';
  String get currentCurrency => _currentCurrency;

  void setLanguage(String language) {
    _currentLanguage = language;
    _syncWithDatabase();
    notifyListeners();
  }

  void setCurrency(String currency) {
    _currentCurrency = currency;
    _syncWithDatabase();
    notifyListeners();
  }

  Future<void> loadUserPreferences() async {
    try {
      final user = Supabase.instance.client.auth.currentUser;
      if (user != null) {
        final response = await Supabase.instance.client
            .schema('cartel')
            .from('profiles')
            .select('language_preference, currency_preference')
            .eq('id', user.id)
            .maybeSingle();

        if (response != null) {
          if (response['language_preference'] != null) {
            _currentLanguage = response['language_preference'];
          }
          if (response['currency_preference'] != null) {
            _currentCurrency = response['currency_preference'];
          }
          notifyListeners();
        }
      }
    } catch (e) {
      debugPrint('Error loading preferences: $e');
    }
  }

  Future<void> _syncWithDatabase() async {
    try {
      final user = Supabase.instance.client.auth.currentUser;
      if (user != null) {
        await Supabase.instance.client
            .schema('cartel')
            .from('profiles')
            .update({
          'language_preference': _currentLanguage,
          'currency_preference': _currentCurrency,
        }).eq('id', user.id);
      }
    } catch (e) {
      debugPrint('Error syncing preferences: $e');
    }
  }

  String formatPrice(double priceInFCFA) {
    if (_currentCurrency == 'USD') {
      double usdPrice = priceInFCFA / 600;
      if (usdPrice >= 1000000) {
        return '${(usdPrice / 1000000).toStringAsFixed(1)}M USD';
      }
      return '\$${usdPrice.toStringAsFixed(0).replaceAllMapped(RegExp(r'(\d{1,3})(?=(\d{3})+(?!\d))'), (Match m) => '${m[1]},')}';
    } else {
      if (priceInFCFA >= 1000000) {
        return '${(priceInFCFA / 1000000).toStringAsFixed(1)}M FCFA';
      }
      return '${priceInFCFA.toStringAsFixed(0).replaceAllMapped(RegExp(r'(\d{1,3})(?=(\d{3})+(?!\d))'), (Match m) => '${m[1]}.')} FCFA';
    }
  }

